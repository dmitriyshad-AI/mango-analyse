"""Phase 2 полуфабрикаты — детерминированные детекторы возражений и тревоги.
Итерация 1. Цель: ловить реальные триггеры, НЕ срабатывать на нейтральные инфо-вопросы.
Это standalone для юнит-проверки; интеграцию в пайплайн делает Кодекс."""
import re

# --- ВОЗРАЖЕНИЯ (item 4) ---
_OBJECTION = {
    "price":   [r"\bдорог", r"дороговат", r"не по карман", r"слишком дорог", r"дешевле", r"цена куса", r"кусается"],
    "think":   [r"\bподума", r"надо подумать", r"посоветуюсь", r"посовет", r"не гото[вв]\w* решить", r"пока думаю"],
    "compare": [r"сравнива", r"у других", r"в другом центре", r"конкурент", r"а чем вы лучше", r"в другом месте"],
    "format":  [r"не увер\w+ в формат", r"очно или онлайн", r"какой формат лучше", r"очно лучше|лучше очно", r"онлайн норм"],
    "distance":[r"\bдалеко\b", r"не наезд", r"долго добира", r"далековато", r"неудобно ездить"],
}
# --- ТРЕВОГА (item 6) ---
_ANXIETY = {
    "capability": [r"не потян", r"не справ", r"боюсь.*слож", r"слишком слож", r"тяжело будет", r"не вытян"],
    "late_start": [r"поздно.{0,8}начин", r"поздно уже", r"упустил", r"поезд ушёл|поезд ушел", r"наверстать"],
    "level_fit":  [r"подойд[её]т ли.{0,20}(ребёнк|ребенк|дочк|сын|уровн|программ|курс|нам|ему|ей|нагрузк)", r"не увер\w+ в уровн", r"справится ли по уровн", r"потянет ли уровень", r"хватит ли.{0,10}знани"],
}
# нейтральные инфо-вопросы — НЕ должны триггерить (анти-ложное)
def _match(text, table):
    t = (text or "").lower()
    for label, pats in table.items():
        for p in pats:
            if re.search(p, t):
                return label
    return None

def detect_objection(text): return _match(text, _OBJECTION)
def detect_anxiety(text):   return _match(text, _ANXIETY)

# ---------------- ТЕСТЫ ----------------
POS_OBJ = [("это дороговато для нас","price"),("слишком дорого","price"),("не по карману честно","price"),
           ("я подумаю и вернусь","think"),("надо подумать, посоветуюсь с мужем","think"),
           ("я сравниваю с другим центром","compare"),("а чем вы лучше других","compare"),
           ("не уверена в формате, очно или онлайн","format"),("какой формат лучше для ОГЭ","format"),
           ("нам далеко добираться","distance"),("долго добираться до вас","distance")]
POS_ANX = [("боюсь, дочка не потянет","capability"),("он не справится, слишком сложно","capability"),
           ("не поздно ли начинать в 9 классе","late_start"),("мы кажется упустили время","late_start"),
           ("подойдёт ли ей этот уровень","level_fit"),("не уверена, справится ли по уровню","level_fit"),
           ("хватит ли его знаний","level_fit")]
NEG = ["сколько стоит годовой курс","когда начинаются занятия","какие документы нужны",
       "есть ли онлайн физика для 9 класса","во сколько занятия по субботам","как записаться на пробное",
       "какой адрес в москве","можно оплатить маткапиталом",
       # адверсариальные ловушки на ложное срабатывание:
       "у вас недорого, мне нравится","расскажите про формат семинара",
       "подойдёт ли время по субботам","какой уровень преподавания у вас","сколько длится одно занятие"]

def run():
    fails=[]
    for txt,exp in POS_OBJ:
        got=detect_objection(txt)
        if got!=exp: fails.append(f"OBJ ждал {exp} получил {got}: {txt}")
    for txt,exp in POS_ANX:
        got=detect_anxiety(txt)
        if got!=exp: fails.append(f"ANX ждал {exp} получил {got}: {txt}")
    for txt in NEG:
        o,a=detect_objection(txt),detect_anxiety(txt)
        if o or a: fails.append(f"ЛОЖНОЕ срабатывание ({o},{a}): {txt}")
    print(f"POS_OBJ={len(POS_OBJ)} POS_ANX={len(POS_ANX)} NEG={len(NEG)}")
    if fails:
        print("ПРОВАЛЫ:", len(fails)); [print("  -",f) for f in fails]
    else:
        print("ВСЁ ЗЕЛЁНОЕ: детекторы ловят триггеры и не ложатся на нейтральное")

if __name__=="__main__": run()
