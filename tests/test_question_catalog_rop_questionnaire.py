import csv
import json
from pathlib import Path

from mango_mvp.question_catalog.rop_questionnaire import (
    QUESTIONNAIRE_COLUMNS,
    build_questionnaire,
    build_questionnaire_rows,
)


def test_questionnaire_rows_are_plain_russian_and_actionable() -> None:
    rows = build_questionnaire_rows(
        [
            {
                "ID класса": "class:refund",
                "Крупный класс": "изменение / отмена услуги",
                "Узкий класс": "возврат денег и штрафы",
                "Класс вопроса": "изменение / отмена услуги / возврат денег и штрафы",
                "Реальные примеры вопросов": "Заявление на возврат. | Верните деньги.",
                "Предлагаемый ответ": "Передам менеджеру для проверки договора.",
                "Что бот может делать": "только менеджер",
                "Нужные актуальные факты": "documents",
                "Источник факта": "doc_1",
                "Какие данные подставить": "[договор] | [сумму оплаты]",
                "Риск ошибки": "высокий",
                "Группы риска": "возврат / перерасчет",
                "Всего вопросов": "415",
                "Много тем в одном вопросе": "нет",
            }
        ]
    )

    row = rows[0]

    assert list(row.keys()) == QUESTIONNAIRE_COLUMNS
    assert row["Блок для РОПа"] == "Возвраты, перерасчеты, отмена"
    assert "415 обращений" in row["Почему это важно"]
    assert "только менеджер" in row["Рекомендация системы"]
    assert "не обещать возврат" in row["Вопрос 4. Какие обещания боту запрещены?"]
    assert row["Ответ РОПа: утвержденная формулировка"] == ""


