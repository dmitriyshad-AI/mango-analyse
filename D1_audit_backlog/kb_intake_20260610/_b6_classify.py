# ---- garble / fragment detection ----
def is_fragment(q):
    n=norm(q)
    # obvious ASR noise tokens
    if 'goggle' in n: return True
    if 'звонок телефона' in n: return True
    if re.search(r'\bабонент\b', n) and len(n)<40: return True
    # very short and contentless echoes
    letters=re.sub(r'[^а-я]','',n)
    if len(letters)<8: return True
    # "Алена, да, где почта вторая, нет, наверное?" type confused echoes handled case-by-case below
    return False

