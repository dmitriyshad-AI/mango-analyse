#!/usr/bin/env python3
"""Генерирует questions_catalog.md из _questions_scan.json (Этап А)."""
import json
d=json.load(open("D1_audit_backlog/kb_intake_20260610/_questions_scan.json",encoding="utf-8"))
s=d["src_stats"]; cl=d["clusters"]
OUT="D1_audit_backlog/kb_intake_20260610/questions_catalog.md"

CLASS_ORDER=[("факт","ФАКТ — вопросы о значениях (закрываются фактом базы)"),
             ("процесс","ПРОЦЕСС — «как/что мне делать» (порядок действий)"),
             ("совет","СОВЕТ/ПОДБОР — «что посоветуете / что подойдёт»"),
             ("P0","P0-МЕНЕДЖЕР — ведёт живой менеджер (это политика, НЕ дыра)")]

def ex_cell(exs):
    out=[]
    for e in exs[:2]:
        q=e["q"].replace("|","/")[:95]
        out.append(f"«{q}» ({e['brand']}/{e['chan']})")
    return "<br>".join(out) if out else "—"

L=[]
L.append("# Каталог реальных вопросов клиентов (Этап А). Клод #2, 2026-06-10\n")
L.append("Вход для сверки покрытия базой (Этап Б). Read-only по корпусам; ПДн замаскированы скриптом ДО вывода "
         "(`<имя>`, `<тел>`, `<почта>`, `<N>`). Бренды раздельно. P0-классы — политика (менеджер), не дыры.\n")
L.append("## Объём обработанного\n")
L.append(f"- **WhatsApp:** {s.get('wa_client_questions')} клиентских сообщений-вопросов (по ВСЕМУ диалогу, не только первые; канал исходящий, период 2022-08…2026-05, фактически затих с янв-2026).")
L.append(f"- **Telegram УНПК:** {s.get('tg_client_questions')} клиентских вопросов (оператор отфильтрован; весь бренд УНПК; сен-2025…май-2026).")
L.append(f"- **Звонки:** {s.get('calls_scanned')} звонков просканировано (client-канал, разбивка на вопросительные предложения; дедуп кластеров по звонку).")
L.append("\n**Единица частоты:** WA/TG — одно клиентское сообщение; звонки — наличие темы-вопроса в звонке (дедуп). "
         "Мультилейбл (сообщение может попасть в несколько кластеров). Счётчики — паттерн-кандидаты, выверены на выборке (верхняя граница), приоритет надёжнее абсолютов.\n")
L.append("**Бренд:** WA — по чату; TG — УНПК; звонки — по маркерам в транскрипте, без маркера → «неопр» (бренд силой не приписан). "
         "Большой «неопр» по звонкам — норма (короткие реплики без брендовых слов).\n")
L.append("**Колонки таблиц:** WA / TG / Звонки — частота по каналам; Ф/У/неопр — бренд (вкл. звонки).\n")

for cls,title in CLASS_ORDER:
    rows=[r for r in cl if r["class"]==cls]
    rows.sort(key=lambda r:-(r["total_chat"]*3+r["call"]))  # вес чату
    L.append(f"\n## {title}\n")
    L.append("| Кластер | WA | TG | Звонки | Ф/У/неопр | Обезличенные примеры |")
    L.append("|---|--:|--:|--:|:--|:--|")
    for r in rows:
        L.append(f"| **{r['label']}** (`{r['cid']}`) | {r['wa']} | {r['tg']} | {r['call']} | "
                 f"{r['foton']}/{r['unpk']}/{r['unknown']} | {ex_cell(r['examples'])} |")

# Хвост
L.append("\n## Хвост: редкие/несклассифицированные вопросы (WA/TG)\n")
L.append("Вопросы, не попавшие в кластеры выше (отдельные темы — кандидаты на новые кластеры или редкий хвост <3). "
         "Замечены повторяющиеся подтемы: **часовой пояс занятий** («время московское?»), **меню/питание лагеря деталь**, "
         "**счёт на юр.лицо**, **отборочный/конкурс**, **страховка для лагеря**, **колледж/проф.ориентация** (вне профиля).\n")
for e in d["tail_sample"][:40]:
    L.append(f"- «{e['q'][:110]}» ({e['brand']}/{e['chan']})")

L.append("\n## Заметки метода и ограничения\n")
L.append("- Маскирование ПДн — скриптом до вывода (телефоны, email, ФИО из 2-3 заглавных слов, «N класс/лет»). Остаточный риск имён в редких формах — низкий.")
L.append("- Регекс-классификатор мультилейбл; выверен на выборке (убраны переловли «договор/справка/кабинет/пропуст» из прошлых итераций).")
L.append("- Звонки: классификация по вопросительным предложениям клиента; бренд по маркерам, большой «неопр».")
L.append("- A_recommend (явный «что посоветуете») в чате редок: клиенты чаще называют класс+предмет и спрашивают про конкретную программу (это уходит в ФАКТ-кластеры), а не просят подбор абстрактно. В звонках подбор выражен сильнее.")
L.append("- P0-классы (возврат-претензия, спор оплаты, жалоба, юр-угроза) даны для полноты, но по политике их ведёт менеджер — в дыры базы НЕ идут.")
open(OUT,"w",encoding="utf-8").write("\n".join(L))
print("written",OUT,"clusters",len(cl))
