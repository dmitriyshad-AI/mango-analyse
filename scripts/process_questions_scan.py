QSTART = re.compile(r"^(как|что|где|когда|куда|можно|какой|какие|подскаж|а\s|почему|нужно|надо)", re.I)


def mask(t):
    t = re.sub(r"[\w.+-]+@[\w-]+\.[\w.-]+","[email]",t)
    t = re.sub(r"\+?\d[\d\s\-()]{8,}\d","[тел]",t)
    t = re.sub(r"\b[А-ЯЁ][а-яё]{2,}(?:\s+[А-ЯЁ][а-яё]{2,}){1,2}\b","[имя]",t)
    t = re.sub(r"(зовут|реб[её]нок|ребёнка|ребенка|сын\w*|доч\w*)\s+[А-ЯЁ][а-яё]+", r"\1 [имя]", t, flags=re.I)
    return re.sub(r"\s+"," ",t).strip()


def brand_calls(text):
    low=(text or "").lower()
    u=any(m in low for m in UNPK_M); f=any(m in low for m in FOTON_M)
    return "unpk" if u and not f else "foton" if f and not u else "unknown"


def add(counts, examples, pid, brand, src, text):
    counts[(pid,brand,src)] += 1
    key=(pid,brand)
    if len(examples[key])<4:
        m=mask(text)[:150]
        if m not in [e[1] for e in examples[key]]:
            examples[key].append((src,m))


def main():
    counts=collections.Counter(); examples=collections.defaultdict(list)
    src_stats={}

    # WhatsApp
    if os.path.exists(WA_DB):
        c=sqlite3.connect(f"file:{WA_DB}?mode=ro",uri=True)
        rows=c.execute("""SELECT m.text, ch.brand_hint FROM messages m JOIN chats ch ON ch.chat_id=m.chat_id
                          WHERE m.role='client' AND m.is_service_message=0""").fetchall(); c.close()
        n=0
        for text,bh in rows:
            t=(text or "").strip()
            if len(t)<8: continue
            if "?" not in t and not QSTART.match(t.lower()): continue
            n+=1
            brand=bh if bh in ("foton","unpk") else "unknown"
            for pid,cat,lab,rx in COMP:
                if rx.search(t): add(counts,examples,pid,brand,"wa",t)
        src_stats["whatsapp_client_q"]=n

    # Telegram УНПК
    if os.path.exists(TG_JSON):
        d=json.load(open(TG_JSON,encoding="utf-8"))
        chats=d.get("chats",{}).get("list",[])
        n=0
        for ch in chats:
            for mm in ch.get("messages",[]):
                if mm.get("type")!="message": continue
                if str(mm.get("from"))=="УНПК МФТИ": continue  # оператор
                txt=mm.get("text")
                if isinstance(txt,list): txt="".join(x if isinstance(x,str) else x.get("text","") for x in txt)
                t=(txt or "").strip()
                if len(t)<8: continue
                if "?" not in t and not QSTART.match(t.lower()): continue
                n+=1
                for pid,cat,lab,rx in COMP:
                    if rx.search(t): add(counts,examples,pid,"unpk","tg",t)
        src_stats["telegram_unpk_client_q"]=n

    # Звонки (client-канал)
    if os.path.exists(CALLS_DB):
        c=sqlite3.connect(f"file:{CALLS_DB}?mode=ro",uri=True)
        n=0
        for tc,tm in c.execute("SELECT transcript_client,transcript_manager FROM canonical_calls WHERE has_transcript_text=1"):
            t=(tc or "").strip()
            if len(t)<8: continue
            n+=1
            brand=brand_calls((tm or "")+" "+(tc or ""))
            for pid,cat,lab,rx in COMP:
                if rx.search(t): add(counts,examples,pid,brand,"call",t)
        c.close()
        src_stats["calls_with_transcript"]=n

    # сводка
    res={"src_stats":src_stats,"rows":[]}
    for pid,cat,lab,rx in ALL:
        row={"pid":pid,"category":cat,"label":lab,"by":{}}
        for src in ("wa","tg","call"):
            for brand in ("foton","unpk","unknown"):
                v=counts[(pid,brand,src)]
                if v: row["by"][f"{src}:{brand}"]=v
        row["total"]=sum(counts[(pid,b,s)] for b in ("foton","unpk","unknown") for s in ("wa","tg","call"))
        ex=[]
        for brand in ("foton","unpk","unknown"):
            for src,m in examples.get((pid,brand),[]):
                ex.append({"brand":brand,"src":src,"q":m})
        row["examples"]=ex[:5]
        res["rows"].append(row)
    json.dump(res,open(OUT,"w",encoding="utf-8"),ensure_ascii=False,indent=1)
    print("Источники (клиентских вопросов/звонков):",src_stats)
    print(f"\n{'pid':4s} {'total':>6s}  процесс")
    for row in sorted(res["rows"],key=lambda r:-r["total"]):
        print(f"{row['pid']:4s} {row['total']:6d}  [{row['category'][:14]}] {row['label']}  {row['by']}")
    print("\nJSON:",OUT)


if __name__=="__main__":
    main()
