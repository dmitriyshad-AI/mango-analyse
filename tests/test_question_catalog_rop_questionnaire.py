def test_build_questionnaire_writes_csv_and_summary(tmp_path: Path) -> None:
    source = tmp_path / "source.csv"
    with source.open("w", encoding="utf-8-sig", newline="") as file:
        writer = csv.DictWriter(
            file,
            fieldnames=[
                "ID класса",
                "Крупный класс",
                "Узкий класс",
                "Класс вопроса",
                "Реальные примеры вопросов",
                "Предлагаемый ответ",
                "Что бот может делать",
                "Нужные актуальные факты",
                "Источник факта",
                "Какие данные подставить",
                "Риск ошибки",
                "Группы риска",
                "Всего вопросов",
                "Много тем в одном вопросе",
                "Приоритетный балл",
            ],
        )
        writer.writeheader()
        writer.writerow(
            {
                "ID класса": "class:format",
                "Крупный класс": "формат обучения",
                "Узкий класс": "онлайн-формат",
                "Класс вопроса": "формат обучения / онлайн-формат",
                "Реальные примеры вопросов": "Можно онлайн?",
                "Предлагаемый ответ": "Проверю актуальный формат.",
                "Что бот может делать": "после проверки актуального факта и утверждения РОПом",
                "Нужные актуальные факты": "location",
                "Источник факта": "location_1",
                "Какие данные подставить": "[курс/предмет]",
                "Риск ошибки": "средний",
                "Группы риска": "нет",
                "Всего вопросов": "10",
                "Много тем в одном вопросе": "нет",
                "Приоритетный балл": "10",
            }
        )

    output = tmp_path / "questionnaire.csv"
    summary_path = tmp_path / "summary.json"

    summary = build_questionnaire(source, output, summary_path)

    assert output.exists()
    assert summary_path.exists()
    assert summary["rows"] == 1
    loaded = json.loads(summary_path.read_text(encoding="utf-8"))
    assert loaded["rows"] == 1


def test_questionnaire_translates_fact_keys_to_russian() -> None:
    rows = build_questionnaire_rows(
        [
            {
                "ID класса": "class:facts",
                "Крупный класс": "расписание",
                "Узкий класс": "расписание без указания группы",
                "Класс вопроса": "расписание / расписание без указания группы",
                "Реальные примеры вопросов": "Когда занятия?",
                "Предлагаемый ответ": "Проверю расписание.",
                "Что бот может делать": "после проверки актуального факта и утверждения РОПом",
                "Нужные актуальные факты": "documents | schedule | price",
                "Источник факта": "fact_1",
                "Какие данные подставить": "нет",
                "Риск ошибки": "средний",
                "Группы риска": "нет",
                "Всего вопросов": "10",
                "Много тем в одном вопросе": "нет",
                "Приоритетный балл": "10",
            }
        ]
    )

    row = rows[0]

    assert "documents" not in row["Вопрос 3. Какие данные бот обязан спросить или проверить?"]
    assert "fact_1" not in row["Вопрос 5. Где брать актуальные факты?"]
    assert "документы и договоры" in row["Вопрос 3. Какие данные бот обязан спросить или проверить?"]
    assert "расписание" in row["Вопрос 5. Где брать актуальные факты?"]


def test_questionnaire_removes_technical_redaction_tokens_and_internal_id() -> None:
    rows = build_questionnaire_rows(
        [
            {
                "ID класса": "question_class:abc",
                "Крупный класс": "оплата / возврат / чек",
                "Узкий класс": "способ оплаты",
                "Класс вопроса": "оплата / возврат / чек / способ оплаты",
                "Реальные примеры вопросов": "Оплата по [PAYMENT_OPTIONS] до [CURRENT_DEADLINE].",
                "Предлагаемый ответ": "Проверить [PAYMENT_OPTIONS].",
                "Что бот может делать": "после проверки актуального факта и утверждения РОПом",
                "Нужные актуальные факты": "documents",
                "Источник факта": "fact_1",
                "Какие данные подставить": "нет",
                "Риск ошибки": "средний",
                "Группы риска": "нет",
                "Всего вопросов": "10",
                "Много тем в одном вопросе": "нет",
                "Приоритетный балл": "10",
            }
        ]
    )

    row = rows[0]

    assert "question_class" not in row
    assert "[PAYMENT_OPTIONS]" not in row["Как клиенты спрашивают"]
    assert "[способы оплаты]" in row["Как клиенты спрашивают"]
    assert "[способы оплаты]" in row["Черновик безопасного ответа"]
