def _structural_context_fragment_override(text: str) -> tuple[str, str, tuple[str, ...]] | None:
    cleaned = clean_text(text)
    if not cleaned:
        return None
    words = re.findall(r"[a-zа-яё0-9]+", cleaned, re.I)
    if GENERIC_COURSE_TITLE_RE.fullmatch(cleaned):
        return "not_enough_context", "обрывок без клиентского вопроса", ()
    if len(words) > STRUCTURAL_FRAGMENT_MAX_WORDS:
        return None
    if CLEAR_CONTEXT_MARKER_RE.search(cleaned):
        return None
    if SHORT_REFERENCE_FRAGMENT_RE.search(cleaned):
        return "incomplete_context", "неполный контекст / уточнение предыдущего сообщения", ()
    if re.fullmatch(r"(?:а\s+)?(?:по|про|насч[её]т|касаемо)\s+[\wё-]+[?!.]?", cleaned, re.I):
        return "incomplete_context", "неполный контекст / уточнение предыдущего сообщения", ()
    return None


