    if re.search(r"\b(?:содержательного\s+запроса\s+от\s+клиента\s+нет|клиент\s+ответил\s+(?:обрывочно|только\s+«?алло)|только\s+«?алло|конкретный\s+запрос\s+по\s+обучению\s+не\s+был\s+раскрыт)\b", text, re.I):
        return "not_enough_context", "обрывок без клиентского вопроса", ()
