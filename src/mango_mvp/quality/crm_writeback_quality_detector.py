def detect_crm_writeback_quality_risks(text: object, *, min_severity: str = "P2") -> list[CrmWritebackFinding]:
    value = _clean(text)
    if not value:
        return [
            CrmWritebackFinding(
                "empty_crm_context",
                "P2",
                "",
                "Empty CRM writeback context",
            )
        ]
    allowed = _allowed_severities(min_severity)
    findings: list[CrmWritebackFinding] = []
    for risk_type, severity, pattern, reason in CRM_WRITEBACK_RULES:
        if severity not in allowed:
            continue
        for match in pattern.finditer(value):
            matched = match.group(0).strip()
            if matched:
                findings.append(CrmWritebackFinding(risk_type, severity, matched, reason))
    return findings


def has_blocking_crm_writeback_quality_risk(text: object, *, min_severity: str = "P2") -> bool:
    return bool(detect_crm_writeback_quality_risks(text, min_severity=min_severity))


def findings_to_risk_counts(findings: Iterable[CrmWritebackFinding]) -> dict[str, int]:
    counts: dict[str, int] = {}
    for finding in findings:
        counts[finding.risk_type] = counts.get(finding.risk_type, 0) + 1
    return counts


def _allowed_severities(min_severity: str) -> set[str]:
    order = {"P0": 0, "P1": 1, "P2": 2, "P3": 3}
    threshold = order.get(min_severity, 2)
    return {severity for severity, rank in order.items() if rank <= threshold}


def _clean(value: object) -> str:
    if value is None:
        return ""
    return re.sub(r"\s+", " ", str(value)).strip()


