def mask(t):
    t=re.sub(r"[\w.+-]+@[\w-]+\.[\w.-]+","<почта>",t)
    t=re.sub(r"\+?\d[\d\s\-()]{8,}\d","<тел>",t)
    t=re.sub(r"\b[А-ЯЁ][а-яё]{2,}(?:\s+[А-ЯЁ][а-яё]{2,}){1,2}\b","<имя>",t)
    t=re.sub(r"(зовут|реб[её]нок|ребёнка|ребенка|сын\w*|доч\w*)\s+[А-ЯЁ][а-яё]+", r"\1 <имя>", t, flags=re.I)
    t=re.sub(r"\b\d{1,2}\s*(класс|лет)\b", r"<N> \1", t)
    return re.sub(r"\s+"," ",t).strip()


def brand_calls(text):
    low=(text or "").lower()
    u=any(m in low for m in UNPK_M); f=any(m in low for m in FOTON_M)
    return "unpk" if u and not f else "foton" if f and not u else "unknown"


def main():
    counts=collections.Counter()          # (cid,brand,chan)
    examples=collections.defaultdict(list) # cid -> [(brand,chan,masked)]
    seen=collections.defaultdict(set)
    tail=[]; tail_seen=set()
    src_stats={}

    def handle(text, brand, chan):
        t=(text or "").strip()
        if len(t)<8: return False
        is_q=("?" in t) or bool(QSTART.match(t.lower()))
        if not is_q: return False
        matched=False
        if not MASTER.search(t):
            if chan in("wa","tg"):
                m=mask(t)[:120]; k=re.sub(r"[^а-яё]","",m.lower())[:40]
                if k and k not in tail_seen and len(tail)<400:
                    tail_seen.add(k); tail.append((brand,chan,m))
            return True
        for cid,cls,lab,rx in COMP:
            if rx.search(t):
                matched=True
                counts[(cid,brand,chan)]+=1
                key=(cid,brand)
                if len(examples[cid])<6:
                    m=mask(t)[:150]
                    if m not in [e[2] for e in examples[cid]]:
                        examples[cid].append((brand,chan,m))
        if not matched and chan in("wa","tg"):
            m=mask(t)[:120]
            k=re.sub(r"[^а-яё]","",m.lower())[:40]
            if k and k not in tail_seen and len(tail)<400:
                tail_seen.add(k); tail.append((brand,chan,m))
        return True

    # WhatsApp — ВСЕ клиентские сообщения
    if os.path.exists(WA_DB):
        c=sqlite3.connect(f"file:{WA_DB}?mode=ro",uri=True); n=0
        for text,bh in c.execute("""SELECT m.text,ch.brand_hint FROM messages m JOIN chats ch ON ch.chat_id=m.chat_id
                                     WHERE m.role='client' AND m.is_service_message=0"""):
            brand=bh if bh in("foton","unpk") else "unknown"
            if handle(text,brand,"wa"): n+=1
        c.close(); src_stats["wa_client_questions"]=n

    # Telegram УНПК
    if os.path.exists(TG_JSON):
        d=json.load(open(TG_JSON,encoding="utf-8")); n=0
        for ch in d.get("chats",{}).get("list",[]):
            for mm in ch.get("messages",[]):
                if mm.get("type")!="message" or str(mm.get("from"))=="УНПК МФТИ": continue
                txt=mm.get("text")
                if isinstance(txt,list): txt="".join(x if isinstance(x,str) else x.get("text","") for x in txt)
                if handle(txt,"unpk","tg"): n+=1
        src_stats["tg_client_questions"]=n

    # Звонки — client канал: бьём на короткие предложения, дедуп кластеров по звонку
    if os.path.exists(CALLS_DB):
        c=sqlite3.connect(f"file:{CALLS_DB}?mode=ro",uri=True); n=0
        lim=int(os.environ.get("CALLS_LIMIT","100000"))
        SENT=re.compile(r"[^.!?…]{8,400}[.!?…]?")
        for tc,tm in c.execute("SELECT transcript_client,transcript_manager FROM canonical_calls WHERE has_transcript_text=1 LIMIT ?",(lim,)):
            if not tc: continue
            n+=1
            brand=brand_calls((tm or "")+" "+(tc or ""))
            seen_cids=set()
            for seg in SENT.findall(tc):
                s=seg.strip()
                if len(s)<10: continue
                if "?" not in s and not QSTART.match(s.lower()): continue
                if not MASTER.search(s): continue
                for cid,cls,lab,rx in COMP:
                    if cid in seen_cids: continue
                    if rx.search(s):
                        seen_cids.add(cid); counts[(cid,brand,"call")]+=1
                        if len(examples[cid])<6:
                            m=mask(s)[:150]
                            if m not in [e[2] for e in examples[cid]]:
                                examples[cid].append((brand,"call",m))
        c.close(); src_stats["calls_scanned"]=n

    res={"src_stats":src_stats,"clusters":[]}
    for cid,cls,lab,rx in TAX:
        row={"cid":cid,"class":cls,"label":lab,"by":{},
             "wa":0,"tg":0,"call":0,"foton":0,"unpk":0,"unknown":0}
        for (k,brand,chan),v in counts.items():
            if k!=cid: continue
            row[chan]+=v; row[brand]+=v
        row["total_chat"]=row["wa"]+row["tg"]
        row["examples"]=[{"brand":b,"chan":ch,"q":m} for b,ch,m in examples[cid][:4]]
        res["clusters"].append(row)
    res["tail_sample"]=[{"brand":b,"chan":ch,"q":m} for b,ch,m in tail[:120]]
    os.makedirs(os.path.dirname(OUT),exist_ok=True)
    json.dump(res,open(OUT,"w",encoding="utf-8"),ensure_ascii=False,indent=1)
    print("Источники:",src_stats)
    print(f"\n{'cid':16s} {'кл':7s} {'чат':>5s} {'звон':>5s} | f/u/unk  метка")
    for r in sorted(res["clusters"],key=lambda x:-(x['total_chat'])):
        print(f"{r['cid']:16s} {r['class']:7s} {r['total_chat']:5d} {r['call']:5d} | {r['foton']}/{r['unpk']}/{r['unknown']}  {r['label']}")
    print("\nхвост несклассифицированного (WA/TG):",len(res["tail_sample"]),"примеров сохранено")
    print("JSON:",OUT)


if __name__=="__main__":
    main()
